public class TabliceDwuwymiarowe {
    public static void main(String[] args) {

        int table[][] = {{1, 2, 3},{2, 3, 4}};
        for (int i = 0; i < table.length; i++) {
            for (int j = 0; j < table[i].length; j++) {
                System.out.println(table[i][j]);
            }
        }

    }


}
